package com.epicare.api;

public class ApiResponse {
    private int id;
    private String title;
    private String body;
    private int userId;

    // Getters
    public int getId() {
        return id;
    }
    public String getTitle() {
        return title;
    }
    public String getBody() {
        return body;
    }
    public int getUserId() {
        return userId;
    }
}
